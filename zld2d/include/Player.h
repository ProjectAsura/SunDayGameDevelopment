#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <cstdint>
#include <asdxTexture.h>
#include <SpriteSystem.h>
#include <UpdateContext.h>
#include <DirectionState.h>
#include <Box.h>


///////////////////////////////////////////////////////////////////////////////
// PLAYER_ACTION
///////////////////////////////////////////////////////////////////////////////
enum PLAYER_ACTION
{
    PLAYER_ACTION_NONE,
    PLAYER_ACTION_ATTACK,
};

///////////////////////////////////////////////////////////////////////////////
// Player class
///////////////////////////////////////////////////////////////////////////////
class Player
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Player();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Player();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //-------------------------------------------------------------------------
    bool Init();

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update(UpdateContext& context);

    //-------------------------------------------------------------------------
    //! @brief      描画処理を行います.
    //-------------------------------------------------------------------------
    void Draw(SpriteSystem& sprite);

    //-------------------------------------------------------------------------
    //! @brief      ダメージを設定します.
    //!
    //! @retval true    死亡.
    //! @retval false   生存.
    //-------------------------------------------------------------------------
    bool ReceiveDamage();

    //-------------------------------------------------------------------------
    //! @brief      スポーンします.
    //-------------------------------------------------------------------------
    void Spawn(int x, int y, bool resetLife = false);

    //-------------------------------------------------------------------------
    //! @brief      ライフ数を取得します.
    //-------------------------------------------------------------------------
    uint8_t GetLife() const
    { return m_Life; }

    //-------------------------------------------------------------------------
    //! @brief      最大ライフ数を取得します.
    //-------------------------------------------------------------------------
    uint8_t GetMaxLife() const
    { return m_MaxLife; }

    //-------------------------------------------------------------------------
    //! @brief      バウンディングボックスを取得します.
    //-------------------------------------------------------------------------
    Box GetBox() const
    { return m_Box; }

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    int                 m_Action        = 0;
    Box                 m_Box           = {};
    Box                 m_HitBox        = {};
    uint8_t             m_Life          = 3;
    uint8_t             m_MaxLife       = 3;
    float               m_AnimLastTime  = 0.0f;
    DIRECTION_STATE     m_Direction     = DIRECTION_LEFT;
    uint8_t             m_AnimFrame     = 0;
    asdx::Texture2D     m_PlayerTexture[12];
    asdx::Texture2D     m_WeaponTexture[4];
    int                 m_NonDamageFrame = 0;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};